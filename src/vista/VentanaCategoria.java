    public void llenartabla() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();

            tablaCategoria.setModel(modelo);

            String sql = "SELECT id_categoria, nombre FROM categoria";
            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            modelo.addColumn("Codigo");
            modelo.addColumn("Nombre de categoria");

            int[] anchos = {5, 50};
            for (int i = 0; i < tablaCategoria.getColumnCount(); i++) {
                tablaCategoria.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
            }

            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        }
    }

