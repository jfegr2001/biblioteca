    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtNombre.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "ESCRIBA EL NOMBRE DE LA CATEGORIA A INGRESAR");
            return;
        }

        String nombre = txtNombre.getText().toUpperCase();

        String tipoMovimiento = "Agrego una categoria";

        Categoria categoria = new Categoria(nombre);
        Movimientos movimientos = new Movimientos(tipoMovimiento);

        try {
            controlador.registrarCategoria(categoria);
            controlador.movimientosAdmin(movimientos);
            JOptionPane.showMessageDialog(null, "NUEVA CATEGORIA AGREGADA");
            limpiarCampo();
            llenartabla();

        } catch (SQLException ex) {
            System.out.print(ex);
        } catch (ExcepcionCategiraYaRegistrada ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ADBERTENCIA", HEIGHT);
        }


    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtNombre.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "ELIJA QUE CATEGORIA DESEA ELIMINAR");
            return;
        }

        String nombre = txtNombre.getText();
        String tipoMovimiento ="Elimino una categoria";
        
        Movimientos movimientos = new Movimientos(tipoMovimiento);
        

        try {
            if (controlador.buscarLibroCategoria(nombre)) {
                
                JOptionPane.showMessageDialog(null, "EXISTEN LIBROS ASOCIADOS A ESA CATEGORIA");

            } else {
                controlador.eliminar(nombre);
                controlador.movimientosAdmin(movimientos);
                JOptionPane.showMessageDialog(null, "CATEGORIA ELIMINADA");
                limpiarCampo();
                llenartabla();
            }

        } catch (SQLException ex) {

            System.out.println(ex.getMessage());
        } catch (ExcepcionNopuedeEliminar e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

